//Assignment operator
let r = 5;

//addition assignment
r = r + 1;
r += 1;


//substraction assignment
r = r - 1;
r =- 1;

//multiplication assignment
r = r * 1;
r *= 1;

//division assignment
r = r / 1;
r /= 1;

//remainder assignment(modules assignment)
r = r % 1;
r %= 1;

//exponential assignment
r = r ** 1;
r **= 1;